#include <iostream>

using namespace std;

int main()
{

    // Variables Declaration

    int a = 10 , b = 5, c;

    // Types of oprators in c++

    // 1. Addition : +
    c = a + b;
    cout<<"The Addion is:"<<c;

    // 2. Subtraction : -
    c = a - b;
    cout<<"\nSubtraction is:"<<c;

    // 3. Multiplication : *
    c =  a * b;
    cout<<"\nMultiplication is:"<<c;

    // 4. Division : /  ,  NOTE : Division of inter numbers will give you a integer result.
    c =  a/b;
    cout<<"\nDivision is:"<<c;

    // 5. Modulo : %   NOTE : returns remainder.
    c =  a%b;
    cout<<"\nRemainder is:"<<c;
    return 0;
}
