#include <iostream>

using namespace std;

int main(){

    // Relational  Operator : Compare two values and return a boolean result.

    //1. equals to : ==

    int a = 10;
    int b = 20;

    cout<<"Is a is equals to b:"<<(a==b);

    // In c++ , True = 1, False = 0.

    // 2. != : Not equals to
    cout<<"\nIs a is not equals to b:"<<(a!=b);

    //3. > , < ,<=,>=

    cout<<"\n(a>b):"<<(a>b);
    cout<<"\n(a<b):"<<(a<b);
    cout<<"\n(a>=b)"<<(a>=b);
    cout<<"\n(a<=b)"<<(a<=b);

return 0;
}
