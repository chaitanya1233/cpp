//1. Initialization: Runs once at the very beginning (e.g., setting a counter to 0).

//2. Condition: Checked before every iteration. If it's true, the loop runs. If false, the loop ends.

//3. Update: Runs at the end of each loop iteration (e.g., adding 1 to your counter).



