

// Use a for loop when you know exactly how many times you want to run a block of code.

//It keeps all its control elements (initialization, condition, and increment/decrement) in one neat little package.

// SYNTAX:

// for (initialization; condition; update) {
    // Code to be executed
// }


#include <iostream>

using namespace std;

int main(){
    for(int i = 1;i <=10;i++){
        cout<<i<<endl;
    }
return 0;
}


